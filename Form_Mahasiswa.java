    public void tampil(){
        DefaultTableModel tabel1= new DefaultTableModel();
        tabel1.addColumn("NIM");
        tabel1.addColumn("Nama");
        tabel1.addColumn("Jenis Kelamin");
        tabel1.addColumn("Alamat");
        tabel1.addColumn("Jurusan");
        tabel1.addColumn("Semester");

        tabel.setModel(tabel1); 
        String cari = txt_cari.getText();
        try {
            ResultSet res= koneksi.stat.executeQuery("select * from mahasiswa where nama like '%"+cari+"%'");           
            while(res.next()){
                tabel1.addRow(new Object[] {
                    res.getString("nim"),
                    res.getString("nama"),
                    res.getString("jenis_kelamin"),
                    res.getString("alamat"),
                    res.getString("jurusan"),
                    res.getString("semester"),
                });
                    tabel.setModel(tabel1);
                }
            } catch (Exception e) {
            
            }    
        }
