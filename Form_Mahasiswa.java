        // TODO add your handling code here:
        String NIM = nim.getText();
        String NAMA = nama.getText();
        String JENISKELAMIN = null;
if (jk_laki.isSelected()) {
    JENISKELAMIN = "Laki-Laki";
} else if (jk_perempuan.isSelected()) {
    JENISKELAMIN = "Perempuan";
}
String JURUSAN  = (String) jurusan.getSelectedItem();
String SEMESTER = semester.getText();
String ALAMAT   = alamat.getText();
        if(NIM.equals("")){
            JOptionPane.showMessageDialog(null, "NIM Harap Diisi");
            nim.requestFocus();
            return;
        }
        if(NAMA.equals("")){
            JOptionPane.showMessageDialog(null, "Nama Pelanggan Harap Diisi");
            nama.requestFocus();
            return;
        }
        if(ALAMAT.equals("")){
            JOptionPane.showMessageDialog(null, "Alamat Harap Diisi");
            alamat.requestFocus();
            return;
        }
        if(JURUSAN.equals("")){
            JOptionPane.showMessageDialog(null, "Jurusan Harap Diisi");
            jurusan.requestFocus();
            return;
        }
        if(SEMESTER.equals("")){
            JOptionPane.showMessageDialog(null, "Semester Harap Diisi");
            semester.requestFocus();
            return;
        }
            else {}
            int konfirm = JOptionPane.showConfirmDialog(this, "Anda Yakin Ingin Menambah Data ?", "Konfirmasi",
                    JOptionPane.YES_NO_OPTION);
             if (konfirm == 0){
                JOptionPane.showMessageDialog(null, "Proses Penyimpanan ?");
        {
        String SQL = "insert into mahasiswa(nim,nama,jenis_kelamin,alamat,jurusan,semester) values ('"+NIM+"','"+NAMA+"', '"+JENISKELAMIN+"','"+ALAMAT+"','"+JURUSAN+"','"+SEMESTER+"')";
        System.out.println(SQL);
        try {
            koneksi.stat.executeUpdate(SQL);
            JOptionPane.showMessageDialog(rootPane, "Data Berhasil Di Tambahkan");
            tampil();
            kosong();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Data Gagal Di Tambahkan");
        }
    }
}
