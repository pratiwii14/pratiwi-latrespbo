    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        nim.setText(tabel.getValueAt(tabel.getSelectedRow(), 0).toString());
        nama.setText(tabel.getValueAt(tabel.getSelectedRow(), 1).toString());
        if ("LAKI-LAKI".equals(tabel.getValueAt(tabel.getSelectedRow(), 2).toString())) {
        jk_laki.setSelected(true);
        } else {
        jk_perempuan.setSelected(true);
        }
        alamat.setText(tabel.getValueAt(tabel.getSelectedRow(), 3).toString());
        jurusan.setSelectedItem(tabel.getValueAt(tabel.getSelectedRow(), 4).toString());
        semester.setText(tabel.getValueAt(tabel.getSelectedRow(), 5).toString());
        
    }//GEN-LAST:event_tabelMouseClicked

